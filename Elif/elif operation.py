a=int(input("Enter a value"))
b=int(input("Enter b value"))
c=input("Enter operation +,-,*,/")
if c=='+':
 print("Addition",a+b)
elif c=='-':
 print("Subtraction",a-b)
elif c=='*':
 print("Multiplication",a*b)

elif c=='/':
  if c!=0:
      print("Division",a/b)
  else:
      print("Not Division")
else:
    print("Invalid Operator")
    
  


            
